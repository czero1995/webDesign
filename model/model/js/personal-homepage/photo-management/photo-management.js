$(function () {
    $('.photo-state').click(function () {
        $(this).toggleClass('active');
        if($(this).hasClass('active')){
            $('.title-u-2').hide();
            $('.title-u-3').show();
        }else{
            $('.title-u-2').show();
            $('.title-u-3').hide();
        }
    })
})